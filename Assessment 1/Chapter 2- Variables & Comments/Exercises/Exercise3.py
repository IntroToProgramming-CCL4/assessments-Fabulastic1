# Storing the name in a variable with whitespaces
name = '\t\n   tyler Durden   \t\n'

# printing the name with the whitespace
print(name)

# Printing the name after using all the strip functions
print(name.lstrip())    # stripping left whitespace

print(name.rstrip())    # stripping right whitespace

print(name.strip())     # stripping whitespaces from both sides
